package aggregation;

public class Aggregation {

	String prod;
	String prodName;
	int prodId;
	String prodColor;
	int prodPrice;
	Aggregation(String prod,String prodName,int prodId,String prodColor,int prodPrice)
	{
		this.prod = prod;
		this.prodName = prodName;
		this.prodId = prodId;
		this.prodColor = prodColor;
		this.prodPrice = prodPrice;
	}

}
